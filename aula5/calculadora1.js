function somar(n1, n2) {
    return n1+n2
}
console.log(somar(10, 20))

function subtrair(n1, n2) {
    return n1-n2
}
console.log(subtrair(10, 20))

function multiplicar(n1, n2) {
    return n1*n2
}
console.log(multiplicar(10, 20))

function dividir(n1, n2) {
    return n1/n2
}
console.log(dividir(10, 20))